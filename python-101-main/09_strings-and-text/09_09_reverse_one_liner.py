# Reverse the string given below in a single line of code
# with the help of string slicing.

palindrome = "too bad i hid a boot"

print(palindrome[::-1])

# another way: 
palindrome = "too bad i hid a boot"
reversed_string = palindrome[::-1]
print(reversed_string)              
